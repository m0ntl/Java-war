package handlers;

import java.util.Optional;

import UI.App;
import UI.UIConstants;
import javafx.event.ActionEvent;
import javafx.event.EventHandler;
import javafx.scene.control.TextInputDialog;
import javafx.stage.Stage;
import javafx.stage.StageStyle;


public class AddMissileLauncherEvent implements EventHandler<ActionEvent>, UIConstants {

	private App app;

	public AddMissileLauncherEvent(App app) {
		this.app = app;
	}
	
	@Override
	public void handle(ActionEvent arg0) {
		
		TextInputDialog dialog = new TextInputDialog("");
		dialog.initStyle(StageStyle.UTILITY);
		dialog.setTitle(ADD_MISSILE_LAUNCHER);
		dialog.setHeaderText("Enter Missile-Launcher's id");
		dialog.setContentText(ID);
		
		Optional<String> idOp = dialog.showAndWait();
		if (idOp.isPresent())
			app.getMainPane().initiateAddMissileLauncher( idOp.get());
		
	}

}
