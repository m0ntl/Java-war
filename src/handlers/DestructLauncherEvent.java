package handlers;

import java.util.Optional;

import UI.App;
import UI.UIConstants;
import javafx.event.ActionEvent;
import javafx.event.EventHandler;
import javafx.scene.control.TextInputDialog;

public class DestructLauncherEvent implements EventHandler<ActionEvent>, UIConstants{

	private App app;

	public DestructLauncherEvent(App app) {
		this.app = app;
	}
	
	@Override
	public void handle(ActionEvent arg0) {
		TextInputDialog dialog = new TextInputDialog("");
		dialog.setTitle(DESTRUCT_LAUNCHER);
		dialog.setHeaderText("Enter Launcher's id");
		dialog.setContentText(ID);

		Optional<String> idOp = dialog.showAndWait();
		if (idOp.isPresent())
			app.getMainPane().initiateDestructLuncher(idOp.get());
	
	}
}
